from numpy import exp, zeros, sum

# ===================================================================================================================================================================================================
def gaussian(l, s):
	g = zeros((1, l))
	m = l/2
	for i in range(0, l):
		g[0, i] = exp(-0.5/float(s) * (i-m)**2)

	return g[0, :] / sum(g)
# ===================================================================================================================================================================================================
